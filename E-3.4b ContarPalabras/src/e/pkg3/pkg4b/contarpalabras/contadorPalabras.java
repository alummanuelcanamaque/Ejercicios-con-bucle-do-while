    private void botonContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonContarActionPerformed
        String frase = areaTexto.getText();
        int contador =0;
        int cantidadPalabras=0;
        
        if (frase.isEmpty()==false){
            do{
                if ((frase.charAt(contador)>='a' && frase.charAt(contador)<='z') || (frase.charAt(contador)>='A' && frase.charAt(contador)<='Z')){
                    if(contador==(frase.length()-1)){
                        cantidadPalabras++; 
                    }else{
                        if ((frase.charAt(contador+1)<'a' || frase.charAt(contador+1)>'z')
                            && (frase.charAt(contador+1)<'A' || frase.charAt(contador+1)>'Z')){
                            cantidadPalabras++; 
                        }
                    }            
                }
                contador++; 
            }while(contador<=(frase.length()-1));
            texto.setText("Hay "+cantidadPalabras+" palabras."); 
        }else{
            texto.setText("Escriba una frase.");
        }
    }//GEN-LAST:event_botonContarActionPerformed

